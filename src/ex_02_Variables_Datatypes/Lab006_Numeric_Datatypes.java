package ex_02_Variables_Datatypes;

public class Lab006_Numeric_Datatypes {
    public static void main(String[] args) {

        // These are all Local variables as well(Based on Scope)
        byte b = 10;
        short s = 100;
        int i = 1000;
        long phone_number1 = 1234567890l;
        long phone_number2 = 9876543210L; // We can use l/L after long literals
        char c1 = 'A';
        char c2 = '@';
        char c3 = 10; // char is an integral datatypes, hence it can also store numeric numbers
        char c4 = '1'; // we can pass single digit number also as a text
        float f1 = 3.14f;
        float f2 = 4.12F; // We can use f/F after floating literals
        double d = 5.199378383;

    }
}


