package ex_02_Variables_Datatypes;

public class Lab001_Variables {
    public static void main(String[] args) {
        byte ageOfAnanda = 30;
        // byte -> Datatype- type of container
        // ageOfAnanda -> Variable Name / Identifier
        // = -> Assignment Operator
        // 30 -> Variable value / Literals
        ageOfAnanda = 40;
        ageOfAnanda = 100;
        System.out.println(ageOfAnanda); // Latest value assigned will be printed

    }
}
