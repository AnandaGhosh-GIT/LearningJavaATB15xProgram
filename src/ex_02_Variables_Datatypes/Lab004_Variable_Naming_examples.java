package ex_02_Variables_Datatypes;

public class Lab004_Variable_Naming_examples {
    static void main(String[] args) {
        int _ = 10; // underscore can be variable (identifier) name
        int $ = 20; // $ sign can be variable (identifier) name
        int year = 2026;
        int this_is_a_VeryLong_variable_name = 1234567890; // no limitation on how long the variable name can be

        int _123 = 123;
        System.out.println(_123); // variable(identifier) naming convention is not violating

       // int enum =10; //Not correct variable name as "enum" is a keyword
    }
}
